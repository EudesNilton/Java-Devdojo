public class aula02TiposPrimitivos {
    public static void main(String[] args) {
        // Tipos primitivos em Java
        // int, double, float, char, boolean
        int intNumber = 10; // Tipo inteiro
        double doubleNumber = 10.5; // Tipo decimal
        float floatNumber = 5.5F; // Tipo float
        char charValue = 'A'; // Tipo caractere
        boolean isBooleanValue = true; // Tipo booleano
        String stringValue = "Olá, Mundo!"; // Tipo String (não é primitivo, mas muito usado)
        
        // Exibindo os valores
        System.out.println("Número Inteiro: " + intNumber);
        System.out.println("Número Decimal: " + doubleNumber);
        System.out.println("Número Float: " + floatNumber);
        System.out.println("Caractere: " + charValue);
        System.out.println("Booleano: " + isBooleanValue);
        System.out.println("String: " + stringValue);
    }
}
